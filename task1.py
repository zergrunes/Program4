import statistics as st
import pandas as pd
df = pd.read_csv("scores.csv")
df.describe(include='all')
print(df)

# max
print('\n******* MAX *******')
max1 = df['Test 1'].max()
print('\nTest 1 Max Score: ' + str(max1))
max2 = df['Test 2'].max()
print('\nTest 2 Max Score: ' + str(max2))
max3 = df['Test 3'].max()
print('\nTest 3 Max Score: ' + str(max3))
max4 = df['Project'].max()
print('\nProject Max Score: ' + str(max4))
# min
print('\n******* MIN *******')
min1 = df['Test 1'].min()
print('\nTest 1 Min Score: ' + str(min1))
min2 = df['Test 2'].min()
print('\nTest 2 Min Score: ' + str(min2))
min3 = df['Test 3'].min()
print('\nTest 3 Min Score: ' + str(min3))
min4 = df['Project'].min()
