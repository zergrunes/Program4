print('\n******* RANGE *******')
print('\n Test 1 Range: ' + str(max1-min1))
print('\n Test 2 Range: ' + str(max2-min2))
print('\n Test 3 Range: ' + str(max3-min3))
print('\n Project Range: ' + str(max4-min4))
